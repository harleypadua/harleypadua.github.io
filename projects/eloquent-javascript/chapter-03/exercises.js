// takes two number paramters
function min(num1, num2) {
  // use Math.min on parameters and return result
return Math.min(num1, num2);
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

// takes a single number parameter
function isEven(num) {
  // use recursion to change negative value of num to be able to act correctly on negative numbers
    if (num < 0) {
        return isEven(-num);
        // check for if 1
    } else if (num === 1) {
        return false;
        // check for if 0
    } else if  (num === 0) {
        return true;
    } else {
      //call isEven(num -2) to go back to the top of the function with the num that was passed in intially 
      //but 2 less than before and it will keep doing that until the number is 1 or 0 to be able to return the boolean value (even or odd).
        return isEven(num - 2);
    }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
  // create variable for counting the characters passed into function as number
  var totalChar = 0;
  // loop through string passed into function
for(var i = string.length - 1; i >= 0; i--) {
  // condition statement for if string has the given character using charAt() method
  if(string.charAt(i) === character) {
    // add each instance to totalChar count
    totalChar += 1;
  }

}
  // return totalChar
  return totalChar;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
return countChars(string, "B");
}

